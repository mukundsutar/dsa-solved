public class P_1234 {
    public static void main(String[] args) {
        int len = 4;

        for (int i = 1; i <= len; i++) {
            System.out.println();
            for (int j = 1; j <= i; j++) {
                System.out.print(j + " ");
            }
        }
    }
}
